// 長い語から当てるためのキー一覧（一度だけ作る）
const FURI_KEYS = Object.keys(FURIGANA).sort((a, b) => b.length - a.length);

/**
 * テキストを「ふつうの文字列」と「{ b: 漢字, r: よみ }」の配列に分解する。
 * 画面側で <ruby> として表示する。未登録の漢字はそのまま文字列で残る。
 */
export function rubyParts(text) {
  const s = text == null ? "" : String(text);
  const parts = [];
  let i = 0;
  let buf = "";
  outer: while (i < s.length) {
    for (const k of FURI_KEYS) {
      if (k && s.startsWith(k, i)) {
        if (buf) { parts.push(buf); buf = ""; }
        parts.push({ b: k, r: FURIGANA[k] });
        i += k.length;
        continue outer;
      }
    }
    buf += s[i];
    i += 1;
  }
  if (buf) parts.push(buf);
  return parts;
}

// ── よみあげ（TTS） ──
//  数学記号は音声エンジンが読みにくいので、読みやすい語に置換してから読む。
const SPEAK_MAP = [
  [/×/g, "かける"], [/÷/g, "わる"], [/＝|=/g, "は"], [/√/g, "ルート"], [/π/g, "パイ"],
  [/²/g, "の2じょう"], [/³/g, "の3じょう"], [/⁴/g, "の4じょう"],
  [/≦|≤/g, "いか"], [/≧|≥/g, "いじょう"], [/±/g, "プラスマイナス"],
  [/−|－|‐|-/g, "マイナス"], [/＋|\+/g, "プラス"], [/°/g, "ど"], [/∠/g, "かく"],
];

function normalizeForSpeech(text) {
  let s = text == null ? "" : String(text);
  for (const [re, rep] of SPEAK_MAP) s = s.replace(re, ` ${rep} `);
  return s.replace(/\s+/g, " ").trim();
}

let _voiceJa = null;
function pickJaVoice() {
  try {
    const vs = window.speechSynthesis.getVoices() || [];
    _voiceJa = vs.find((v) => /ja|japan/i.test(v.lang)) || null;
  } catch { _voiceJa = null; }
}

/** 問題文を読み上げる（対応していない端末では何もしない）。 */
export function speakText(text) {
  try {
    if (!("speechSynthesis" in window)) return;
    const synth = window.speechSynthesis;
    synth.cancel(); // 直前の読み上げを止める（重ならないように）
    if (!_voiceJa) pickJaVoice();
    const u = new SpeechSynthesisUtterance(normalizeForSpeech(text));
    u.lang = "ja-JP";
    u.rate = 0.95;
    if (_voiceJa) u.voice = _voiceJa;
    synth.speak(u);
  } catch { /* 失敗しても無視（音声非対応端末など） */ }
}

/** 読み上げを止める。 */
export function stopSpeak() {
  try { if ("speechSynthesis" in window) window.speechSynthesis.cancel(); } catch { /* noop */ }
}

/** この端末で読み上げが使えるか。 */
export function speechAvailable() {
  try { return "speechSynthesis" in window; } catch { return false; }
}
